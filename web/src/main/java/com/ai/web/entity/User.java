package com.ai.web.entity;


public class User{
  private String personId;
  private String nickName;
  private String headPicture;
  private String doubleGame;
  private String pkTimes;
public String getPersonId() {
	return personId;
}
public void setPersonId(String personId) {
	this.personId = personId;
}
public String getNickName() {
	return nickName;
}
public void setNickName(String nickName) {
	this.nickName = nickName;
}
public String getHeadPicture() {
	return headPicture;
}
public void setHeadPicture(String headPicture) {
	this.headPicture = headPicture;
}
public String getDoubleGame() {
	return doubleGame;
}
public void setDoubleGame(String doubleGame) {
	this.doubleGame = doubleGame;
}
public String getPkTimes() {
	return pkTimes;
}
public void setPkTimes(String pkTimes) {
	this.pkTimes = pkTimes;
}
  
}
